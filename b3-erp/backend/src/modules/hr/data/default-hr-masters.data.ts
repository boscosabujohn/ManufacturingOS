// ============================================
// ENUMS
// ============================================

export enum DepartmentStatus {
  ACTIVE = 'Active',
  INACTIVE = 'Inactive',
}

export enum DesignationLevel {
  ENTRY_LEVEL = 'Entry Level',
  JUNIOR = 'Junior',
  INTERMEDIATE = 'Intermediate',
  SENIOR = 'Senior',
  LEAD = 'Lead',
  MANAGER = 'Manager',
  SENIOR_MANAGER = 'Senior Manager',
  DIRECTOR = 'Director',
  VICE_PRESIDENT = 'Vice President',
  SENIOR_VICE_PRESIDENT = 'Senior Vice President',
  C_LEVEL = 'C-Level Executive',
}

export enum DesignationStatus {
  ACTIVE = 'Active',
  INACTIVE = 'Inactive',
}

export enum LeaveTypeStatus {
  ACTIVE = 'Active',
  INACTIVE = 'Inactive',
}

export enum LeaveAccrualType {
  ANNUAL = 'Annual',
  MONTHLY = 'Monthly',
  QUARTERLY = 'Quarterly',
  NONE = 'None',
}

export enum ShiftType {
  GENERAL = 'General',
  ROTATING = 'Rotating',
  FLEXIBLE = 'Flexible',
  NIGHT = 'Night',
}

export enum ShiftStatus {
  ACTIVE = 'Active',
  INACTIVE = 'Inactive',
}

// ============================================
// INTERFACES
// ============================================

export interface DefaultDepartment {
  code: string;
  name: string;
  description?: string;
  parentDepartmentCode?: string;
  location?: string;
  email?: string;
  phone?: string;
  status: DepartmentStatus;
}

export interface DefaultDesignation {
  code: string;
  title: string;
  description?: string;
  level: DesignationLevel;
  reportsToCode?: string;
  gradeLevel: number;
  minSalary?: number;
  maxSalary?: number;
  responsibilities?: string[];
  requiredSkills?: string[];
  qualifications?: string[];
  minExperience?: number;
  status: DesignationStatus;
}

export interface DefaultLeaveType {
  code: string;
  name: string;
  description?: string;
  isPaid: boolean;
  maxDaysPerYear: number;
  minDaysPerApplication: number;
  maxDaysPerApplication?: number;
  maxCarryForward: number;
  allowCarryForward: boolean;
  allowEncashment: boolean;
  maxEncashmentDays: number;
  requiresApproval: boolean;
  requiresDocument: boolean;
  minAdvanceNoticeDays: number;
  maxBackdatedDays: number;
  accrualType: LeaveAccrualType;
  accrualRate: number;
  allowHalfDay: boolean;
  allowNegativeBalance: boolean;
  includeHolidays: boolean;
  includeWeekends: boolean;
  applicableAfterMonths: number;
  displayOrder: number;
  color?: string;
  status: LeaveTypeStatus;
}

export interface DefaultShift {
  code: string;
  name: string;
  description?: string;
  type: ShiftType;
  startTime: string;
  endTime: string;
  workingHours: number;
  breakStartTime?: string;
  breakEndTime?: string;
  breakHours: number;
  graceMinutes: number;
  lateMarkAfterMinutes: number;
  halfDayAfterMinutes: number;
  absentAfterMinutes: number;
  workingDays: string[];
  allowOvertime: boolean;
  overtimeMultiplier: number;
  isNightShift: boolean;
  nightShiftAllowance: number;
  status: ShiftStatus;
}

